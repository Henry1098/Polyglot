    def __init__(self) -> None:
        with open('other_files/it/irrV.txt','r',encoding="utf-8") as f:
            self.irregularverb=[i.strip() for i in f.readlines()]
        with open('other_files/it/irrNouns.txt','r',encoding="utf-8") as f2:
            self.irregularnouns=[i.strip() for i in f2.readlines()]

    def irregularverb_(self,tokens):
        if tokens in self.irregularverb:
            return "True"
        else:
            return "False"   

    def irregularnoun_(self,token):
        if token in self.irregularnouns:
            return "True"
        else:
            return "False"

    def groupeVerbe(self,token_):
        groupeVerbe = 0
        strLemmaF = ""
        strLemmaF = token_[-3:]
        strLemma = token_

        if strLemmaF == "are":
            groupeVerbe = 1
        if strLemmaF == "ere":
            groupeVerbe = 2
        if strLemmaF is "ire" and strLemma not in self.iscVerbs:
            groupeVerbe = 31
        elif strLemma in self.iscVerbs:
            groupeVerbe = 32
        return groupeVerbe